# Declare constants
DAYS_IN_A_YEAR = 365
HOURS_IN_A_DAY = 24
MINUTES_IN_AN_HOUR= 60

# Compute how minutes in a year
minutes_in_a_year = DAYS_IN_A_YEAR * HOURS_IN_A_DAY * MINUTES_IN_AN_HOUR

# Display the output
puts "There are %d minutes in one year." % minutes_in_a_year

